
print("\n10 & 0 : ", 10 & 0)        #10 & 0 :  0

print("\n10 | 0 : ", 10 | 0)        #10 | 0 :  10

print("\n10 ^ 0 : ", 10 ^ 0)        #10 ^ 0 :  10

print("\n~10 : ", ~10)              #~10 :  -11

print("\n6 << 2 : ", 6 << 2)        #6 << 2 :  24

print("\n8 >> 2 : ", 8 >> 2)        #8 >> 2 :  2
