
Name = "Prathamesh Bapu Gaikwad"

print("\n'Gaikwad' in Name : ", "Gaikwad" in Name)      #'Gaikwad' in Name :  True

print("\n'akash' in Name : ", "akash" in Name)          #'akash' in Name :  False

List = [1, 2, 3, 4]

print("\n3 in List : ", 3 in List)                      #3 in List :  True

print("\n5 in List : ", 5 in List)                      #5 in List :  False

Tuple = ('a', 'b', 'c', 'd')

print("\n'a' in Tuple : ", 'a' in Tuple)                #'a' in Tuple :  True

print("\n'z' in Tuple : ", 'z' in Tuple)                #'z' in Tuple :  False

Dict = {1 : Name , 2 : List, 3 : Tuple}             

print("\n1 in Dict : ", 1 in Dict)                      #1 in Dict :  True

print("\nName in Dict : ", Name in Dict)                #Name in Dict :  False
